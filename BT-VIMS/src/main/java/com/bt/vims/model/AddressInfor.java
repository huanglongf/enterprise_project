package com.bt.vims.model;

import java.io.Serializable;
import java.util.Date;

public class AddressInfor implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 4879249664607900127L;
	private Integer id; // 主键
	private String addressCode; // 地址编码
	private String addressName; // 地址名称
	private String printerIp; // 打印机名称
	private String remark; // 备注
	private Integer isDisabled; // 启用标记 0:启用 1:禁用
	private Integer isDeleted; // 数据状态 0:正常 1:已删除
	private Date createTime; // 创建时间
	private Date updateTime; // 更新时间
	private String createBy; // 创建人
	private String updateBy; // 更新人
	private Integer isStartPrint; //是否调用打印机打印（0：打印，1：不打印）
	
	
	public AddressInfor() {
	}

	public void setParam(Integer isDisabled, Integer isDeleted, Date createTime, Date updateTime, String createBy, String updateBy){
		this.isDisabled = isDisabled;
		this.isDeleted = isDeleted;
		this.createTime = createTime;
		this.updateTime = updateTime;
		this.createBy = createBy;
		this.updateBy = updateBy;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getAddressCode() {
		return addressCode;
	}

	public void setAddressCode(String addressCode) {
		this.addressCode = addressCode;
	}

	public String getAddressName() {
		return addressName;
	}

	public void setAddressName(String addressName) {
		this.addressName = addressName;
	}

	public String getPrinterIp() {
		return printerIp;
	}

	public void setPrinterIp(String printerIp) {
		this.printerIp = printerIp;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Integer getIsDisabled() {
		return isDisabled;
	}

	public void setIsDisabled(Integer isDisabled) {
		this.isDisabled = isDisabled;
	}

	public Integer getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(Integer isDeleted) {
		this.isDeleted = isDeleted;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getUpdateBy() {
		return updateBy;
	}

	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}

	public Integer getIsStartPrint() {
		return isStartPrint;
	}

	public void setIsStartPrint(Integer isStartPrint) {
		this.isStartPrint = isStartPrint;
	}
	
	

}
