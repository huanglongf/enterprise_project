package com.bt.wms.model;

import com.bt.wms.utils.QueryParameter;

public class RecordQueryParam  extends QueryParameter{
	public String create_user;
	public String container_code;
	public String location;
	public String sku;
	public String create_time_s;
	public String create_time_e;
	public String type;
	public String getCreate_user() {
		return create_user;
	}
	public void setCreate_user(String create_user) {
		this.create_user = create_user;
	}
	public String getContainer_code() {
		return container_code;
	}
	public void setContainer_code(String container_code) {
		this.container_code = container_code;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getSku() {
		return sku;
	}
	public void setSku(String sku) {
		this.sku = sku;
	}
	public String getCreate_time_s() {
		return create_time_s;
	}
	public void setCreate_time_s(String create_time_s) {
		this.create_time_s = create_time_s;
	}
	public String getCreate_time_e() {
		return create_time_e;
	}
	public void setCreate_time_e(String create_time_e) {
		this.create_time_e = create_time_e;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
