package com.bt.workOrder.bean;

import java.util.Date;

public class WoMutualLog {
    private int id;

    private String createBy;

    private Date createTime;

    private String woNo;

    private String action;

    private String obligate;
    
    private String log;

    private String processRemark;

    private String accessory;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getWoNo() {
        return woNo;
    }

    public void setWoNo(String woNo) {
        this.woNo = woNo;
    }

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	public String getObligate() {
		return obligate;
	}

	public void setObligate(String obligate) {
		this.obligate = obligate;
	}

	public String getLog() {
		return log;
	}

	public void setLog(String log) {
		this.log = log;
	}

	public String getProcessRemark() {
		return processRemark;
	}

	public void setProcessRemark(String processRemark) {
		this.processRemark = processRemark;
	}

	public String getAccessory() {
		return accessory;
	}

	public void setAccessory(String accessory) {
		this.accessory = accessory;
	}

	@Override
	public String toString() {
		return "WoMutualLog [id=" + id + ", createBy=" + createBy + ", createTime=" + createTime + ", woNo=" + woNo
				+ ", action=" + action + ", obligate=" + obligate + ", log=" + log + ", processRemark=" + processRemark
				+ ", accessory=" + accessory + "]";
	}
	
    
}