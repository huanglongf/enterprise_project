public class WorkOrderEventMonitor {
	private String id;
	private String create_by;
	private String create_time;
	private String update_by;
	private String update_time;
	private String wo_id;
	private String result_info;
	private String file_path;
	private String sort_store;
	private String out_result;
	private String out_result_reason;
	// 事件发生顺讯
	private int sort;
	// 事件编码
	private String event;
	// 事件状态
	private Boolean event_status;
	// 事件描述
	private String event_description;
	// 是否计入工时
	private Boolean outManhours;
	// 相关说明
	private String remark;
	public WorkOrderEventMonitor() {
		
	}
	public WorkOrderEventMonitor(String id, String create_by, String wo_id, int sort, String event, Boolean event_status, String event_description) {
		this.id = id;
		this.create_by = create_by;
		this.wo_id = wo_id;
		this.sort = sort;
		this.event = event;
		this.event_status = event_status;
		this.event_description = event_description;
		
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCreate_by() {
		return create_by;
	}
	public void setCreate_by(String create_by) {
		this.create_by = create_by;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getUpdate_by() {
		return update_by;
	}
	public void setUpdate_by(String update_by) {
		this.update_by = update_by;
	}
	public String getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}
	public String getWo_id() {
		return wo_id;
	}
	public void setWo_id(String wo_id) {
		this.wo_id = wo_id;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public Boolean getEvent_status() {
		return event_status;
	}
	public void setEvent_status(Boolean event_status) {
		this.event_status = event_status;
	}
	public String getEvent_description() {
		return event_description;
	}
	public void setEvent_description(String event_description) {
		this.event_description = event_description;
	}
	public Boolean getOutManhours() {
		return outManhours;
	}
	public void setOutManhours(Boolean outManhours) {
		this.outManhours = outManhours;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getResult_info() {
		return result_info;
	}
	public void setResult_info(String result_info) {
		this.result_info = result_info;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getSort_store() {
		return sort_store;
	}
	public void setSort_store(String sort_store) {
		this.sort_store = sort_store;
	}
	public String getOut_result() {
		return out_result;
	}
	public void setOut_result(String out_result) {
		this.out_result = out_result;
	}
	public String getOut_result_reason() {
		return out_result_reason;
	}
	public void setOut_result_reason(String out_result_reason) {
		this.out_result_reason = out_result_reason;
	}
	
}
