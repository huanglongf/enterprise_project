package com.bt.lmis.model;

import java.io.Serializable;
import java.util.Date;

public class CustomerPageBean implements Serializable{
	/**
	 * 用户自定义页面组件实体类
	 */
	private static final long serialVersionUID = 1L;
	
	//列编码(数据库字段)
	private String column_code;
	//列名称
	private String column_name; 
	//列类型
	private String date_type;
	//页面标识Id
	private Integer page_id;
	//所属用户
	private Integer user_id;
	//创建时间
	private Date creare_date;
	//创建用户
	private Integer create_user_id;
	//组件类型 [1:查询条件 2:显示列]
	private Integer assembly_type;
	//组件序号
	private Integer assembly_sort;
	//组件样式
	private Integer assembly_style;
	
	public String getColumn_code() {
		return column_code;
	}
	public void setColumn_code(String column_code) {
		this.column_code = column_code;
	}
	public String getColumn_name() {
		return column_name;
	}
	public void setColumn_name(String column_name) {
		this.column_name = column_name;
	}
	public String getDate_type() {
		return date_type;
	}
	public void setDate_type(String date_type) {
		this.date_type = date_type;
	}
	public Integer getPage_id() {
		return page_id;
	}
	public void setPage_id(Integer page_id) {
		this.page_id = page_id;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public Date getCreare_date() {
		return creare_date;
	}
	public void setCreare_date(Date creare_date) {
		this.creare_date = creare_date;
	}
	public Integer getCreate_user_id() {
		return create_user_id;
	}
	public void setCreate_user_id(Integer create_user_id) {
		this.create_user_id = create_user_id;
	}
	public Integer getAssembly_type() {
		return assembly_type;
	}
	public void setAssembly_type(Integer assembly_type) {
		this.assembly_type = assembly_type;
	}
	public Integer getAssembly_sort() {
		return assembly_sort;
	}
	public void setAssembly_sort(Integer assembly_sort) {
		this.assembly_sort = assembly_sort;
	}
	public Integer getAssembly_style() {
		return assembly_style;
	}
	public void setAssembly_style(Integer assembly_style) {
		this.assembly_style = assembly_style;
	}
}
