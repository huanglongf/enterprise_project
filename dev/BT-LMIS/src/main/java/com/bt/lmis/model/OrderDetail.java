package com.bt.lmis.model;

public class OrderDetail {
	private int id;
	private Integer bat_id;
	private String express_number;
	private String sku_number;
	private String barcode;
	private String item_name;
	private String extend_pro;
	private String qty;
	private String create_date;
	private String operation_time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Integer getBat_id() {
		return bat_id;
	}
	public void setBat_id(Integer bat_id) {
		this.bat_id = bat_id;
	}
	public String getExpress_number() {
		return express_number;
	}
	public void setExpress_number(String express_number) {
		this.express_number = express_number;
	}
	public String getSku_number() {
		return sku_number;
	}
	public void setSku_number(String sku_number) {
		this.sku_number = sku_number;
	}
	public String getBarcode() {
		return barcode;
	}
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getExtend_pro() {
		return extend_pro;
	}
	public void setExtend_pro(String extend_pro) {
		this.extend_pro = extend_pro;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getOperation_time() {
		return operation_time;
	}
	public void setOperation_time(String operation_time) {
		this.operation_time = operation_time;
	}
}
