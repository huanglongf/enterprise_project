public class BalanceReportExpressParam {
	private String verId;
	private String conId;
	private String expressCode;
	private String expressName;
	private String balanceMonth;
	public BalanceReportExpressParam() {}
	public BalanceReportExpressParam(String conId, String expressCode, String expressName, String balanceMonth) {
		this.conId = conId;
		this.expressCode = expressCode;
		this.expressName = expressName;
		this.balanceMonth = balanceMonth;
		
	}
	public String getVerId() {
		return verId;
	}
	public void setVerId(String verId) {
		this.verId = verId;
	}
	public String getConId() {
		return conId;
	}
	public void setConId(String conId) {
		this.conId = conId;
	}
	public String getExpressCode() {
		return expressCode;
	}
	public void setExpressCode(String expressCode) {
		this.expressCode = expressCode;
	}
	public String getExpressName() {
		return expressName;
	}
	public void setExpressName(String expressName) {
		this.expressName = expressName;
	}
	public String getBalanceMonth() {
		return balanceMonth;
	}
	public void setBalanceMonth(String balanceMonth) {
		this.balanceMonth = balanceMonth;
	}
	
}
