package com.bt.orderPlatform.model.sfIntraCity.element;


public class BasicPriceRule{
    
    private String weight_upper;

    private String distance_upper;

    public BasicPriceRule(String weight_upper, String distance_upper){
        this.weight_upper = weight_upper;
        this.distance_upper = distance_upper;
    }

    public String getWeight_upper(){
        return weight_upper;
    }

    public void setWeight_upper(String weight_upper){
        this.weight_upper = weight_upper;
    }

    public String getDistance_upper(){
        return distance_upper;
    }

    public void setDistance_upper(String distance_upper){
        this.distance_upper = distance_upper;
    }
}
