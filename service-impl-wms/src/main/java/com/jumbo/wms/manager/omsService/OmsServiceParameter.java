package com.jumbo.wms.manager.omsService;



/**
 * OMS接口方法
 */

public class OmsServiceParameter {
    private String username;
    private String password;
    private String source;
    private String secretkey;
    private String ouid;
    private String soCode;
    private String transCode;
    private String goodsWeight;
    private String transportatorCode;
    
    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getSource() {
        return source;
    }
    public void setSource(String source) {
        this.source = source;
    }
    public String getSecretkey() {
        return secretkey;
    }
    public void setSecretkey(String secretkey) {
        this.secretkey = secretkey;
    }
    public String getOuid() {
        return ouid;
    }
    public void setOuid(String ouid) {
        this.ouid = ouid;
    }
    public String getSoCode() {
        return soCode;
    }
    public void setSoCode(String soCode) {
        this.soCode = soCode;
    }
    public String getTransCode() {
        return transCode;
    }
    public void setTransCode(String transCode) {
        this.transCode = transCode;
    }
    public String getGoodsWeight() {
        return goodsWeight;
    }
    public void setGoodsWeight(String goodsWeight) {
        this.goodsWeight = goodsWeight;
    }
    public String getTransportatorCode() {
        return transportatorCode;
    }
    public void setTransportatorCode(String transportatorCode) {
        this.transportatorCode = transportatorCode;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
}
